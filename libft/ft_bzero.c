#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	m;
	char	*cast;

	m = 0;
	cast = (char *)s;
	if (n != 0)
	{
		while (m < n)
		{
			cast[m] = '\0';
			m++;
		}
	}
}
