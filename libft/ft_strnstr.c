#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;
	char	*cast;

	i = 0;
	cast = (char *)big;
	if (little[0] == '\0')
		return (cast);
	while (big[i] && i < len)
	{
		j = 0;
		while (big[i + j] == little[j] && little[j] && i + j < len)
			j++;
		if (little[j] == '\0')
			return (&cast[i]);
		i++;
	}
	return (0);
}
